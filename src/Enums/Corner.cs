namespace Tavenem.Blazor.Framework;

/// <summary>
/// A corner of the view.
/// </summary>
public enum Corner
{
    /// <summary>
    /// An unspecified corner.
    /// </summary>
    None = 0,

    /// <summary>
    /// The top-left corner.
    /// </summary>
    Top_Left = 1,

    /// <summary>
    /// The top-right corner.
    /// </summary>
    Top_Right = 3,

    /// <summary>
    /// The bottom-left corner.
    /// </summary>
    Bottom_Left = 7,

    /// <summary>
    /// The bottom-right corner.
    /// </summary>
    Bottom_Right = 9,
}

namespace Tavenem.Blazor.Framework;

/// <summary>
/// Indicates whether an element is located at the top or bottom.
/// </summary>
public enum VerticalSide
{
    /// <summary>
    /// No side specified.
    /// </summary>
    None = 0,

    /// <summary>
    /// The top.
    /// </summary>
    Top = 1,

    /// <summary>
    /// The bottom.
    /// </summary>
    Bottom = 2,
}

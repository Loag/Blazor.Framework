namespace Tavenem.Blazor.Framework;

/// <summary>
/// A side on which an element may be located.
/// </summary>
public enum Side
{
    /// <summary>
    /// No side specified.
    /// </summary>
    None = 0,

    /// <summary>
    /// The left side.
    /// </summary>
    Left = 1,

    /// <summary>
    /// The right side.
    /// </summary>
    Right = 2,

    /// <summary>
    /// The top.
    /// </summary>
    Top = 3,

    /// <summary>
    /// The bottom.
    /// </summary>
    Bottom = 4,
}
